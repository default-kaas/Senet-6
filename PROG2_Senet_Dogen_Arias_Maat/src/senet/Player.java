package senet;

public class Player {
	private String name;
	private int colorsign;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public int getColorsign() {
		return colorsign;
	}
	public void setColorsign(int colorsign) {
		this.colorsign = colorsign;
	}
	
	public String toString() {
		return name;
	}
}
